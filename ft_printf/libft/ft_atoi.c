#include "ft_printf.h"

int		ft_atoi(char **str)
{
	int		result;
	int		sign;
	int		i;

	result = 0;
	sign = 1;
	i = 0;
	while (((*str)[i] >= '0' && (*str)[i] <= '9'))
	{
		result = (result * 10) + ((*str)[i] - '0');
		i++;
	}
	(*str) = &(*str)[i];
	return (result * sign);
}
